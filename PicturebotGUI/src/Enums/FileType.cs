namespace PicturebotGUI.src.Enums
{
    /// <summary>
    /// String enum containing different file types in order to create a new shoot
    /// </summary>
    public static class FileType
    {
        /// <summary>
        /// Create a shoot containing RAW file types
        /// </summary>
        public static string RAW { get { return "RAW"; } }
        /// <summary>
        /// Create a shoot containing compressed file types
        /// </summary>
        public static string Compressed { get { return "Compressed"; } }
    }
}
